package modelo;

/**
 *
 * @author Alumno_2DAW
 */
public class Pelicula {
    private int id;
    private String title;

    public Pelicula(int id, String title) {
        this.id = id;
        this.title = title;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }
    
}
